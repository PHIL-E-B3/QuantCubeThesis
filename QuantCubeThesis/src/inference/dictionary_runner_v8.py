DOC_SEPARATOR = '\n\n'
# 6. (Optional) Override text extraction from a JSON doc dict
def get_text(doc: dict) -> str:
    for field in ('prepared_remarks', 'text', 'content', 'body', 'statement', 'speech'):
        if field in doc and isinstance(doc[field], str):
            return doc[field]
    return ' '.join(str(v) for v in doc.values() if isinstance(v, str))


