# CELL 6 — PATH HELPERS + COVERAGE CHECK  (do not edit)
#
# Temporal alignment rules (based on fomc_calendar.csv structure):
# ──────────────────────────────────────────────────────────────────────────
# Each calendar row X corresponds to meeting_date[X].
#
# statements  → released ON meeting_date[X]
#               Store on row X (no lag). The Sentiment Tester's shift(-1)
#               on the dependent variable then aligns statement[X] with
#               decision[X+1]. Correct.
#
# conferences → press conference on meeting_date[X], same logic as statements.
#               Store on row X (no lag).
#
# minutes     → released on minutes_date[X], ~3 weeks AFTER meeting_date[X].
#               Not public before meeting X. Store on row X+1 (lag = 1),
#               meaning they serve as prior info for the NEXT meeting.
#
# speeches    → blackout_date[X] is a few days BEFORE meeting_date[X].
#               Capture speeches in (blackout_date[X-1], blackout_date[X]].
#               These are available before the blackout of meeting X → row X.
# ===========================================================================

# lag=0: store on the row matching this meeting (statement/conference/speeches)
# lag=1: store on the NEXT row (minutes, published after the meeting)
SOURCE_LAG = {
    'statements':  0,
    'conferences': 0,
    'minutes':     1,
    'speeches':    0,
}


def _speech_paths(idx: int) -> list:
    """Speeches in the inter-meeting window ending at blackout_date[X]."""
    upper = calendar.loc[idx, 'blackout_date']
    lower = (
        calendar.loc[idx - 1, 'blackout_date'] if idx > 0
        else pd.Timestamp('1900-01-01')
    )
    if pd.isna(upper):
        return []
    if pd.isna(lower):
        lower = pd.Timestamp('1900-01-01')
    return [
        path
        for dt, file_list in catalogue['speeches'].items()
        if lower < dt <= upper
        for path in file_list
    ]


def _point_paths(idx: int, src: str) -> list:
    """
    Fetch the JSON file for `src` that belongs on calendar row `idx`.

    SOURCE_LAG controls the lookup:
      lag=0 → fetch the file dated at calendar row idx (same meeting)
      lag=1 → fetch the file dated at calendar row idx-1 (previous meeting,
               because the source was published after that meeting ended)
    """
    lag        = SOURCE_LAG.get(src, 0)
    target_idx = idx - lag
    if target_idx < 0:
        return []
    target = calendar.loc[target_idx, FILE_DATE_COL[src]]
    if pd.isna(target):
        return []
    return catalogue[src].get(target, [])


def get_paths(idx: int) -> dict:
    return {
        src: (_speech_paths(idx) if src == 'speeches' else _point_paths(idx, src))
        for src in active_sources
    }


