def _extract_date(fname: str):
    """Return pd.Timestamp from YYYYMMDD in filename, else None."""
    m = re.search(r'(\d{8})', fname)
    if m:
        try:
            return pd.to_datetime(m.group(1), format='%Y%m%d').normalize()
        except ValueError:
            pass
    return None


def _index_directory(directory: str, subdirs=None) -> dict:
    """Return {date: [path, ...]} for all dated JSON files in directory."""
    search_dirs = (
        [os.path.join(directory, s) for s in subdirs] if subdirs else [directory]
    )
    index = {}
    for d in search_dirs:
        if not os.path.exists(d):
            print(f'  Warning: directory not found: {d}')
            continue
        for fname in os.listdir(d):
            if not fname.endswith('.json'):
                continue
            dt = _extract_date(fname)
            if dt is not None:
                index.setdefault(dt, []).append(os.path.join(d, fname))
    return index


def _load_text(paths: list) -> str:
    """Load JSON files and concatenate their text fields."""
    texts = []
    for path in paths:
        with open(path) as f:
            data = json.load(f)
        docs = data if isinstance(data, list) else [data]
        for doc in docs:
            t = get_text(doc).strip()
            if t:
                texts.append(t)
    return ' '.join(texts)


